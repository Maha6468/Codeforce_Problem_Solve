package Sheet1;

import java.util.Scanner;

public class C {
    public static void main(String[] args) {
    Scanner input = new Scanner(System.in);
    long X = input.nextLong();
    long Y = input.nextLong();
    System.out.println(X + " + " + Y + " = " + (X + Y));
    System.out.println(X + " * " + Y + " = " + (X * Y));
    System.out.println(X + " - " + Y + " = " + (X - Y));

    }
}
